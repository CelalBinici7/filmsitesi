
package entity;


public class sss {
    private int sss_id;
    private String soru;
    private String cevap;

    public sss(int id, String soru, String cevap) {
        this.sss_id = sss_id;
        this.soru = soru;
        this.cevap = cevap;
    }

    public sss() {
    }

    public int getId() {
        return sss_id;
    }

    public void setId(int id) {
        this.sss_id = sss_id;
    }

    public String getSoru() {
        return soru;
    }

    public void setSoru(String soru) {
        this.soru = soru;
    }

    public String getCevap() {
        return cevap;
    }

    public void setCevap(String cevap) {
        this.cevap = cevap;
    }
    
}
